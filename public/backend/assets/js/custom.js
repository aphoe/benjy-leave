/**
 * Change the first letters of a string to uppercase
 * @param str
 * @returns {string}
 */
function ucwords (str) {
    return (str + '').replace(/^([a-z])|\s+([a-z])/g, function ($1) {
        return $1.toUpperCase();
    });
}

/**
 * jQuery equivalent of laravel's str_limit()
 * @param str
 * @param lim
 * @returns {*}
 */
function str_limit(str, lim){
    lim = lim || 40;
    if(str > lim){
        str = str.substr(0, lim) + '...';
    }
    return str;
}

